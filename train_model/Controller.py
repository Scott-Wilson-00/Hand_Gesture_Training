def get_args():
    parser = argparse.ArgumentParser()

    parser.add_argument("--device", type=int, default=0)
    parser.add_argument("--width", help='cap width', type=int, default=960)
    parser.add_argument("--height", help='cap height', type=int, default=540)

    parser.add_argument('--use_static_image_mode', action='store_true')
    parser.add_argument("--min_detection_confidence",
                        help='min_detection_confidence',
                        type=float,
                        default=0.7)
    parser.add_argument("--min_tracking_confidence",
                        help='min_tracking_confidence',
                        type=int,
                        default=0.5)

    args = parser.parse_args()

    return args

MODEL_PATH = 'train_model/static/trained_model.pt'
GESTURE = 'pointingR'
CLASS_TO_INDEX = {"fistL": 0, "fistR": 1, "okayL": 2, "okayR": 3, "peaceL": 4, "peaceR": 5, "pointingL": 6, "pointingR": 7}
INDEX_TO_CLASS = {"0": "fistL", "1": "fistR", "2": "okayL", "3": "okayR", "4": "peaceL", "5": "peaceR", "6": "pointingL", "7": "pointingR"}

def main():
    # Argument parsing #################################################################
    args = get_args()
    print(1)
    cap_device = args.device
    cap_width = args.width
    cap_height = args.height

    use_static_image_mode = args.use_static_image_mode
    min_detection_confidence = args.min_detection_confidence
    min_tracking_confidence = args.min_tracking_confidence

    # Camera preparation ###############################################################
    cap = cv.VideoCapture(cap_device)
    cap.set(cv.CAP_PROP_FRAME_WIDTH, cap_width)
    cap.set(cv.CAP_PROP_FRAME_HEIGHT, cap_height)

    # Model load #############################################################
    mp_hands = mp.solutions.hands
    hands = mp_hands.Hands(
        static_image_mode=use_static_image_mode,
        max_num_hands=2,
        min_detection_confidence=min_detection_confidence,
        min_tracking_confidence=min_tracking_confidence,
    )

    neural_network = GestureNeuralNetwork()
    neural_network.load_state_dict(torch.load(MODEL_PATH))
    neural_network.eval()

    # Coordinate history #################################################################
    history_length = 16
    point_history = deque(maxlen=history_length)

    #  ########################################################################
    analysis_mode = False

    print(2)
    while True:

        # Process Key (ESC: end) #################################################
        key = cv.waitKey(10)
        if key == 27:  # ESC
            break
        elif key == 107: # k
            analysis_mode = toggle_analysis(analysis_mode)

        # Camera capture #####################################################
        ret, image = cap.read()
        attempts = 0;
        while not ret and attempts < 5:
            attempts+=1
            print(f"FAILED to capture frame, retrying: attempt {attempts}")
            ret, image = cap.read()
            
        image = cv.flip(image, 1)  # Mirror display
        #debug_image = copy.deepcopy(image)

        # Detection implementation #############################################################
        image = cv.cvtColor(image, cv.COLOR_BGR2RGB)

        image.flags.writeable = False
        results = hands.process(image)
        image.flags.writeable = True

        #  ####################################################################
        if results.multi_hand_landmarks is not None:
            for hand_landmarks in results.multi_hand_landmarks:

                # Landmark calculation
                #landmark_list = calc_landmark_list(debug_image, hand_landmarks)
                landmark_list_3d = calc_landmark_list_3d(hand_landmarks)

                # 
                # use model to make guess
                tensor = torch.tensor(landmark_list_3d)
                tensor = torch.flatten(tensor, 0)
                pred = neural_network(tensor)
                map_prediction_to_keyboard(pred)
                    

                # Drawing part
                #debug_image = draw_landmarks(debug_image, landmark_list)
        else:
            point_history.append([0, 0])

        # Screen reflection #############################################################
        #cv.imshow('Hand Gesture Recognition', debug_image)

    cap.release()
    cv.destroyAllWindows()


def calc_landmark_list(image, landmarks):
    image_width, image_height = image.shape[1], image.shape[0]

    landmark_point = []

    # Keypoint
    for _, landmark in enumerate(landmarks.landmark):
        landmark_x = min(int(landmark.x * image_width), image_width - 1)
        landmark_y = min(int(landmark.y * image_height), image_height - 1)
        # landmark_z = landmark.z

        landmark_point.append([landmark_x, landmark_y])

    return landmark_point

def calc_landmark_list_3d(landmarks):
    landmark_point = []
    first = True

    # Keypoint
    for _, landmark in enumerate(landmarks.landmark):
        if first:
            first = False
        else:
            landmark_point.append([landmark.x, landmark.y, -landmark.z])

    return landmark_point

def toggle_analysis(mode):
    print("SWAPPED ANALYSIS MODE TO " + str(not mode))
    return not mode



