"""
Cursor:
        Up:    point right
        Down:  fist  right
        Right: peace right
        Left:  fist  left

MouseClick
        Left:  point left
        Right: peace left
"""
def map_prediction_to_keyboard(predictions):
    max_index = torch.argmax(predictions).item()
    pred_gesture = INDEX_TO_CLASS[str(max_index)]
    print(pred_gesture)
    
    if pred_gesture[-1] == "R":
        match pred_gesture:
                case "peaceR":
                        pyautogui.move(10, 0, _pause=False)
                case "okayR":
                        #pyautogui.move(-10, 0, _pause=False)
                        # don't do anything so you can keep the hand up
                        pass
                case "pointingR":
                        pyautogui.move(0, -10, _pause=False)
                case "fistR":
                        pyautogui.move(0, 10, _pause=False)
    elif pred_gesture[-1] == "L": 
        match pred_gesture:
                case "peaceL":
                        pyautogui.rightClick()
                case "okayL":
                        pass
                case "pointingL":
                        pyautogui.leftClick()
                case "fistL":
                        pyautogui.move(-10, 0, _pause=False)


    print(pred_gesture)
    

